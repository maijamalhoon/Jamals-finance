import {
  existsSync,
  mkdirSync,
  readFileSync,
  readdirSync,
  rmSync,
  statSync,
  writeFileSync,
} from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

import ts from "typescript";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const legacyModule = "@/components/icons/jalvoro/compat";
const generatedRuntimePath = path.join(
  root,
  "components/icons/jalvoro/lucide-runtime.generated.tsx",
);
const compatPath = path.join(root, "components/icons/jalvoro/compat.tsx");
const sourceCleanupTestPath = path.join(
  root,
  "components/icons/jalvoro/source-cleanup.test.ts",
);

const ignoredDirectories = new Set([
  ".git",
  ".next",
  ".vercel",
  "build",
  "coverage",
  "design of  UI",
  "node_modules",
  "out",
  "qa-screenshots",
]);
const sourceExtensions = new Set([".js", ".jsx", ".mjs", ".cjs", ".ts", ".tsx"]);

function sourceFiles(directory) {
  return readdirSync(directory).flatMap((entry) => {
    if (ignoredDirectories.has(entry)) return [];
    const absolute = path.join(directory, entry);
    if (statSync(absolute).isDirectory()) return sourceFiles(absolute);
    if (!sourceExtensions.has(path.extname(entry))) return [];
    if (
      absolute === generatedRuntimePath ||
      absolute === compatPath ||
      absolute === fileURLToPath(import.meta.url)
    ) {
      return [];
    }
    return [absolute];
  });
}

function scriptKind(file) {
  if (file.endsWith(".tsx")) return ts.ScriptKind.TSX;
  if (file.endsWith(".jsx")) return ts.ScriptKind.JSX;
  if (file.endsWith(".js") || file.endsWith(".mjs") || file.endsWith(".cjs")) {
    return ts.ScriptKind.JS;
  }
  return ts.ScriptKind.TS;
}

function classifyType(name) {
  if (/node/i.test(name)) return "node";
  if (/(props|attributes)/i.test(name)) return "props";
  return "component";
}

function rewriteImports() {
  const typeNames = new Map();
  let rewrittenFiles = 0;
  let rewrittenDeclarations = 0;

  for (const file of sourceFiles(root)) {
    const sourceText = readFileSync(file, "utf8");
    if (!sourceText.includes("lucide-react")) continue;

    const source = ts.createSourceFile(
      file,
      sourceText,
      ts.ScriptTarget.Latest,
      true,
      scriptKind(file),
    );
    const edits = [];

    for (const statement of source.statements) {
      const isLucideImport =
        ts.isImportDeclaration(statement) &&
        ts.isStringLiteral(statement.moduleSpecifier) &&
        statement.moduleSpecifier.text === "lucide-react";
      const isLucideExport =
        ts.isExportDeclaration(statement) &&
        statement.moduleSpecifier &&
        ts.isStringLiteral(statement.moduleSpecifier) &&
        statement.moduleSpecifier.text === "lucide-react";

      if (!isLucideImport && !isLucideExport) continue;

      const moduleSpecifier = statement.moduleSpecifier;
      edits.push({
        start: moduleSpecifier.getStart(source) + 1,
        end: moduleSpecifier.getEnd() - 1,
        value: legacyModule,
      });
      rewrittenDeclarations += 1;

      if (isLucideImport && statement.importClause) {
        const clause = statement.importClause;
        if (clause.isTypeOnly && clause.name) {
          typeNames.set(clause.name.text, classifyType(clause.name.text));
        }
        const bindings = clause.namedBindings;
        if (bindings && ts.isNamedImports(bindings)) {
          for (const element of bindings.elements) {
            if (clause.isTypeOnly || element.isTypeOnly) {
              const importedName = (element.propertyName ?? element.name).text;
              typeNames.set(importedName, classifyType(importedName));
            }
          }
        }
      }

      if (
        isLucideExport &&
        statement.isTypeOnly &&
        statement.exportClause &&
        ts.isNamedExports(statement.exportClause)
      ) {
        for (const element of statement.exportClause.elements) {
          const exportedName = (element.propertyName ?? element.name).text;
          typeNames.set(exportedName, classifyType(exportedName));
        }
      }
    }

    if (edits.length === 0) continue;
    let updated = sourceText;
    for (const edit of edits.sort((left, right) => right.start - left.start)) {
      updated = `${updated.slice(0, edit.start)}${edit.value}${updated.slice(edit.end)}`;
    }
    writeFileSync(file, updated, "utf8");
    rewrittenFiles += 1;
  }

  return { typeNames, rewrittenFiles, rewrittenDeclarations };
}

function createCompatModule(typeNames) {
  if (!existsSync(generatedRuntimePath)) {
    throw new Error(
      "Generated JALVORO runtime is missing. Run npm run generate:icons before the cleanup script.",
    );
  }

  let runtime = readFileSync(generatedRuntimePath, "utf8")
    .replace(
      "/* This file is generated by scripts/generate-jalvoro-lucide-runtime.mjs. */",
      "/* First-party JALVORO compatibility names retained for source stability. */",
    )
    .replace(
      'import type { JalvoroIconComponent, JalvoroIconProps } from "./types";',
      'import type { JalvoroIconComponent, JalvoroIconNode, JalvoroIconProps } from "./types";',
    );

  const aliases = [...typeNames.entries()]
    .sort(([left], [right]) => left.localeCompare(right))
    .map(([name, kind]) => {
      if (kind === "node") {
        return `export type ${name} = readonly JalvoroIconNode[];`;
      }
      if (kind === "props") {
        return `export type ${name} = JalvoroIconProps;`;
      }
      return `export type ${name} = JalvoroIconComponent;`;
    })
    .join("\n");

  runtime = runtime.replace(
    "\nexport default icons;\n",
    `\n${aliases ? `${aliases}\n\n` : ""}export default icons;\n`,
  );

  mkdirSync(path.dirname(compatPath), { recursive: true });
  writeFileSync(compatPath, runtime, "utf8");
  rmSync(generatedRuntimePath, { force: true });
}

function cleanNextConfig() {
  const configPath = path.join(root, "next.config.ts");
  let source = readFileSync(configPath, "utf8");
  source = source.replace('import path from "node:path";\n\n', "");
  source = source.replace(
    /const jalvoroLucideRuntime\s*=\s*[\s\S]*?;\nconst jalvoroLucideRuntimeAbsolute\s*=\s*path\.resolve\([\s\S]*?\);\n\n/,
    "",
  );
  source = source.replace(
    /  turbopack: \{\n    resolveAlias: \{\n      "lucide-react": jalvoroLucideRuntime,\n    \},\n  \},\n  webpack\(config\) \{[\s\S]*?\n  \},\n/,
    "",
  );
  if (/jalvoroLucideRuntime|"lucide-react":/.test(source)) {
    throw new Error("Failed to remove the temporary Lucide resolver alias from next.config.ts.");
  }
  writeFileSync(configPath, source, "utf8");
}

function cleanPackageJson() {
  const packagePath = path.join(root, "package.json");
  const packageJson = JSON.parse(readFileSync(packagePath, "utf8"));

  delete packageJson.scripts["generate:icons"];
  delete packageJson.scripts.postinstall;
  delete packageJson.scripts.predev;
  if (typeof packageJson.scripts.prebuild === "string") {
    packageJson.scripts.prebuild = packageJson.scripts.prebuild
      .replace(/^npm run generate:icons\s*&&\s*/, "")
      .trim();
    if (!packageJson.scripts.prebuild) delete packageJson.scripts.prebuild;
  }
  delete packageJson.dependencies["lucide-react"];

  writeFileSync(packagePath, `${JSON.stringify(packageJson, null, 2)}\n`, "utf8");
}

function cleanGitignore() {
  const ignorePath = path.join(root, ".gitignore");
  let source = readFileSync(ignorePath, "utf8");
  source = source.replace(
    /\n# Generated JALVORO compatibility module\n\/components\/icons\/jalvoro\/lucide-runtime\.generated\.tsx\n/,
    "\n",
  );
  writeFileSync(ignorePath, source, "utf8");
}

