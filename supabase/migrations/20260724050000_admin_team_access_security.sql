create table if not exists private.platform_admin_invitations (
  id uuid primary key default gen_random_uuid(),
  invitation_code text not null unique default (
    'AIN-' || upper(substr(replace(gen_random_uuid()::text, '-', ''), 1, 12))
  ),
  token_sha256 bytea not null unique,
  intended_email_sha256 bytea not null,
  intended_email_masked text not null,
  role text not null,
  status text not null default 'pending',
  created_by uuid not null references auth.users(id) on delete cascade,
  accepted_by uuid references auth.users(id) on delete set null,
  created_at timestamptz not null default now(),
  expires_at timestamptz not null,
  accepted_at timestamptz,
  revoked_at timestamptz,
  constraint platform_admin_invitations_code_check check (
    invitation_code ~ '^AIN-[A-F0-9]{12}$'
  ),
  constraint platform_admin_invitations_token_hash_check check (
    octet_length(token_sha256) = 32
  ),
  constraint platform_admin_invitations_email_hash_check check (
    octet_length(intended_email_sha256) = 32
  ),
  constraint platform_admin_invitations_mask_check check (
    char_length(intended_email_masked) between 5 and 160
  ),
  constraint platform_admin_invitations_role_check check (
    role in ('admin', 'analyst', 'support')
  ),
  constraint platform_admin_invitations_status_check check (
    status in ('pending', 'accepted', 'revoked', 'expired')
  ),
  constraint platform_admin_invitations_expiry_check check (
    expires_at > created_at
    and expires_at <= created_at + interval '7 days'
  ),
  constraint platform_admin_invitations_acceptance_check check (
    (status = 'accepted' and accepted_at is not null and accepted_by is not null)
    or (status <> 'accepted' and accepted_at is null)
  ),
  constraint platform_admin_invitations_revocation_check check (
    (status = 'revoked' and revoked_at is not null)
    or (status <> 'revoked' and revoked_at is null)
  )
);

comment on table private.platform_admin_invitations is
