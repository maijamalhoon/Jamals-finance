create or replace function private.mask_platform_admin_email(p_email text)
returns text
language sql
immutable
strict
set search_path = pg_catalog
as $$
  select
    case
      when position('@' in p_email) <= 1 then 'hidden'
      else
        left(split_part(lower(trim(p_email)), '@', 1), 1)
        || repeat(
          '*',
          greatest(
            2,
            least(char_length(split_part(lower(trim(p_email)), '@', 1)) - 1, 6)
          )
        )
        || '@'
        || split_part(lower(trim(p_email)), '@', 2)
    end;
$$;

revoke all on function private.mask_platform_admin_email(text)
  from public, anon, authenticated;
grant execute on function private.mask_platform_admin_email(text)
  to service_role;

