create or replace function private.create_platform_admin_invitation(
  p_email text,
  p_role text,
  p_token_sha256 text,
  p_expires_in_hours integer default 72
)
returns jsonb
language plpgsql
security definer
set search_path = pg_catalog, auth, private, extensions
as $$
declare
  v_actor uuid := auth.uid();
  v_actor_role text;
  v_email text := lower(trim(coalesce(p_email, '')));
  v_email_hash bytea;
  v_token_hash bytea;
  v_invitation private.platform_admin_invitations%rowtype;
begin
  select role into v_actor_role
  from private.platform_admins
  where user_id = v_actor and disabled_at is null;

  if v_actor is null or v_actor_role <> 'owner' then
    raise exception 'owner_access_required' using errcode = '42501';
  end if;

  if char_length(v_email) < 5
    or char_length(v_email) > 254
    or v_email !~ '^[^[:space:]@]+@[^[:space:]@]+\.[^[:space:]@]+$' then
    raise exception 'admin_invitation_email_invalid' using errcode = '22023';
  end if;

  if p_role not in ('admin', 'analyst', 'support') then
    raise exception 'admin_invitation_role_invalid' using errcode = '22023';
  end if;

  if p_expires_in_hours < 1 or p_expires_in_hours > 168 then
    raise exception 'admin_invitation_expiry_invalid' using errcode = '22023';
  end if;

  if p_token_sha256 !~ '^[a-f0-9]{64}$' then
    raise exception 'admin_invitation_token_invalid' using errcode = '22023';
  end if;

  v_email_hash := extensions.digest(convert_to(v_email, 'UTF8'), 'sha256');
  v_token_hash := decode(p_token_sha256, 'hex');

  update private.platform_admin_invitations
  set status = 'expired'
  where status = 'pending' and expires_at <= now();

  update private.platform_admin_invitations
  set status = 'revoked', revoked_at = now()
  where status = 'pending'
    and intended_email_sha256 = v_email_hash;

  insert into private.platform_admin_invitations (
    token_sha256,
    intended_email_sha256,
    intended_email_masked,
    role,
    created_by,
    expires_at
  ) values (
    v_token_hash,
    v_email_hash,
    private.mask_platform_admin_email(v_email),
    p_role,
    v_actor,
    now() + make_interval(hours => p_expires_in_hours)
  ) returning * into v_invitation;

  insert into private.platform_admin_access_audit (
    actor_user_id,
    invitation_id,
    action,
    next_role
  ) values (
    v_actor,
    v_invitation.id,
    'invitation_created',
    v_invitation.role
  );

  return jsonb_build_object(
    'invitationCode', v_invitation.invitation_code,
    'maskedEmail', v_invitation.intended_email_masked,
    'role', v_invitation.role,
    'expiresAt', v_invitation.expires_at
  );
end;
$$;

