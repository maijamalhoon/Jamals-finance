create or replace function private.purge_expired_admin_access_data(
  p_initiated_by text default 'service_role'
)
returns jsonb
language plpgsql
security definer
set search_path = pg_catalog, private
as $$
declare
  v_audit_deleted bigint := 0;
  v_invites_deleted bigint := 0;
begin
  if p_initiated_by not in ('scheduled', 'service_role', 'manual') then
    raise exception 'admin_access_retention_initiator_invalid' using errcode = '22023';
  end if;

  delete from private.platform_admin_access_audit
  where expires_at <= now();
  get diagnostics v_audit_deleted = row_count;

  delete from private.platform_admin_invitations
  where (
    status in ('accepted', 'revoked', 'expired')
    and created_at <= now() - interval '24 months'
  ) or (
    status = 'pending'
    and expires_at <= now() - interval '30 days'
  );
  get diagnostics v_invites_deleted = row_count;

  return jsonb_build_object(
    'auditEventsDeleted', v_audit_deleted,
    'invitationsDeleted', v_invites_deleted,
    'ranAt', now()
  );
end;
$$;

revoke all on function private.purge_expired_admin_access_data(text)
  from public, anon, authenticated;
grant execute on function private.purge_expired_admin_access_data(text)
  to service_role;

