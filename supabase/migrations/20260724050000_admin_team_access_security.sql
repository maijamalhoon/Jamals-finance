create or replace function private.accept_platform_admin_invitation(
  p_token_sha256 text
)
returns jsonb
language plpgsql
security definer
set search_path = pg_catalog, auth, private, extensions
as $$
declare
  v_user_id uuid := auth.uid();
  v_email text;
  v_email_hash bytea;
  v_token_hash bytea;
  v_invitation private.platform_admin_invitations%rowtype;
  v_existing_role text;
begin
  if v_user_id is null then
    raise exception 'authentication_required' using errcode = '42501';
  end if;

  if p_token_sha256 !~ '^[a-f0-9]{64}$' then
    raise exception 'admin_invitation_token_invalid' using errcode = '22023';
  end if;

  select lower(trim(email)) into v_email
  from auth.users
  where id = v_user_id and deleted_at is null;

  if v_email is null or v_email = '' then
    raise exception 'verified_email_required' using errcode = '42501';
  end if;

  v_email_hash := extensions.digest(convert_to(v_email, 'UTF8'), 'sha256');
  v_token_hash := decode(p_token_sha256, 'hex');

  update private.platform_admin_invitations
  set status = 'expired'
  where status = 'pending' and expires_at <= now();

  select * into v_invitation
  from private.platform_admin_invitations
  where token_sha256 = v_token_hash
    and intended_email_sha256 = v_email_hash
    and status = 'pending'
    and expires_at > now()
  for update;

  if not found then
    raise exception 'admin_invitation_not_found' using errcode = 'P0002';
  end if;

  select role into v_existing_role
  from private.platform_admins
  where user_id = v_user_id and disabled_at is null;

  if v_existing_role = 'owner' then
    raise exception 'owner_role_protected' using errcode = '42501';
  end if;

  insert into private.platform_admins (
    user_id,
    role,
    created_by,
    disabled_at
  ) values (
    v_user_id,
    v_invitation.role,
    v_invitation.created_by,
    null
  )
  on conflict (user_id) do update
  set role = excluded.role,
      created_by = excluded.created_by,
      disabled_at = null;

  update private.platform_admin_invitations
  set status = 'accepted',
      accepted_by = v_user_id,
      accepted_at = now()
  where id = v_invitation.id;

  insert into private.platform_admin_access_audit (
    actor_user_id,
    subject_user_id,
    invitation_id,
    action,
    previous_role,
    next_role
  ) values (
    v_user_id,
    v_user_id,
    v_invitation.id,
    'invitation_accepted',
    v_existing_role,
    v_invitation.role
  );

  return jsonb_build_object(
    'accepted', true,
    'role', v_invitation.role,
    'redirectTo', '/admin'
  );
end;
$$;

