create or replace function private.apply_platform_admin_member_action(
  p_admin_reference text,
  p_action text,
  p_role text default null
)
returns jsonb
language plpgsql
security definer
set search_path = pg_catalog, auth, private
as $$
declare
  v_actor uuid := auth.uid();
  v_actor_role text;
  v_target private.platform_admins%rowtype;
  v_target_reference text := upper(trim(coalesce(p_admin_reference, '')));
  v_next_role text;
  v_active_owner_count bigint;
begin
  select role into v_actor_role
  from private.platform_admins
  where user_id = v_actor and disabled_at is null;

  if v_actor is null or v_actor_role <> 'owner' then
    raise exception 'owner_access_required' using errcode = '42501';
  end if;

  select pa.* into v_target
  from private.platform_admins pa
  where private.platform_admin_reference(pa.user_id) = v_target_reference
  for update;

  if not found then
    raise exception 'admin_member_not_found' using errcode = 'P0002';
  end if;

  if v_target.user_id = v_actor then
    raise exception 'admin_self_change_blocked' using errcode = '42501';
  end if;

  select count(*)::bigint into v_active_owner_count
  from private.platform_admins
  where role = 'owner' and disabled_at is null;

  if p_action = 'set_role' then
    if p_role not in ('owner', 'admin', 'analyst', 'support') then
      raise exception 'admin_role_invalid' using errcode = '22023';
    end if;

    if v_target.role = 'owner'
      and v_target.disabled_at is null
      and p_role <> 'owner'
      and v_active_owner_count <= 1 then
      raise exception 'last_owner_protected' using errcode = '42501';
    end if;

    v_next_role := p_role;
    update private.platform_admins
    set role = v_next_role
    where user_id = v_target.user_id;

    insert into private.platform_admin_access_audit (
      actor_user_id,
      subject_user_id,
      action,
      previous_role,
      next_role
    ) values (
      v_actor,
      v_target.user_id,
      'role_changed',
      v_target.role,
      v_next_role
    );

  elsif p_action = 'disable' then
    if v_target.disabled_at is not null then
      raise exception 'admin_already_disabled' using errcode = '22023';
    end if;

    if v_target.role = 'owner' and v_active_owner_count <= 1 then
      raise exception 'last_owner_protected' using errcode = '42501';
    end if;

    update private.platform_admins
    set disabled_at = now()
    where user_id = v_target.user_id;

    insert into private.platform_admin_access_audit (
      actor_user_id,
      subject_user_id,
      action,
      previous_role
    ) values (
      v_actor,
      v_target.user_id,
      'access_disabled',
      v_target.role
    );

  elsif p_action = 'restore' then
    if v_target.disabled_at is null then
      raise exception 'admin_already_active' using errcode = '22023';
    end if;

    update private.platform_admins
    set disabled_at = null
    where user_id = v_target.user_id;

    insert into private.platform_admin_access_audit (
      actor_user_id,
      subject_user_id,
      action,
      next_role
    ) values (
      v_actor,
      v_target.user_id,
      'access_restored',
      v_target.role
    );
  else
    raise exception 'admin_action_invalid' using errcode = '22023';
  end if;

  return jsonb_build_object(
    'adminReference', v_target_reference,
    'action', p_action,
    'role', coalesce(v_next_role, v_target.role)
  );
end;
$$;

