create or replace function private.get_admin_access_snapshot()
returns jsonb
language plpgsql
stable
security definer
set search_path = pg_catalog, auth, private
as $$
declare
  v_actor uuid := auth.uid();
  v_actor_role text;
begin
  select role into v_actor_role
  from private.platform_admins
  where user_id = v_actor and disabled_at is null;

  if v_actor is null or v_actor_role is null then
    raise exception 'admin_access_required' using errcode = '42501';
  end if;

  return jsonb_build_object(
    'operationsAllowed', v_actor_role = 'owner',
    'inviteDelivery', 'manual_code',
    'rawInviteTokenStored', false,
    'userMetadataAuthorization', false,
    'serviceRoleExposedToBrowser', false,
    'counts', jsonb_build_object(
      'activeOwners', (
        select count(*)::bigint from private.platform_admins
        where role = 'owner' and disabled_at is null
      ),
      'activeAdmins', (
        select count(*)::bigint from private.platform_admins
        where role = 'admin' and disabled_at is null
      ),
      'activeAnalysts', (
        select count(*)::bigint from private.platform_admins
        where role = 'analyst' and disabled_at is null
      ),
      'activeSupport', (
        select count(*)::bigint from private.platform_admins
        where role = 'support' and disabled_at is null
      ),
      'disabledMembers', (
        select count(*)::bigint from private.platform_admins
        where disabled_at is not null
      ),
      'pendingInvitations', (
        select count(*)::bigint from private.platform_admin_invitations
        where status = 'pending' and expires_at > now()
      )
    ),
    'members', coalesce((
      select jsonb_agg(
        jsonb_build_object(
          'adminReference', private.platform_admin_reference(rows.user_id),
          'maskedEmail', private.mask_platform_admin_email(rows.email),
          'role', rows.role,
          'status', case when rows.disabled_at is null then 'active' else 'disabled' end,
          'createdAt', rows.created_at,
          'lastAccessAt', rows.last_access_at,
          'isSelf', rows.user_id = v_actor,
          'manageable', v_actor_role = 'owner' and rows.user_id <> v_actor
        ) order by
          case rows.role when 'owner' then 1 when 'admin' then 2 when 'analyst' then 3 else 4 end,
          rows.created_at
      )
      from (
        select
          pa.user_id,
          au.email,
          pa.role,
          pa.created_at,
          pa.disabled_at,
          (
            select max(aal.created_at)
            from private.admin_access_log aal
            where aal.admin_user_id = pa.user_id
          ) as last_access_at
        from private.platform_admins pa
        join auth.users au on au.id = pa.user_id
        where au.deleted_at is null
        order by pa.created_at
        limit 50
      ) rows
    ), '[]'::jsonb),
    'invitations', coalesce((
      select jsonb_agg(
        jsonb_build_object(
          'invitationCode', invitation_code,
          'maskedEmail', intended_email_masked,
          'role', role,
          'status', case when expires_at <= now() then 'expired' else status end,
          'createdAt', created_at,
          'expiresAt', expires_at,
          'manageable', v_actor_role = 'owner' and status = 'pending' and expires_at > now()
        ) order by created_at desc
      )
      from (
        select *
        from private.platform_admin_invitations
        where status = 'pending'
        order by created_at desc
        limit 30
      ) invites
    ), '[]'::jsonb),
    'recentEvents', coalesce((
      select jsonb_agg(
        jsonb_build_object(
          'eventReference', 'AAE-' || lpad(event_id::text, 10, '0'),
          'action', action,
          'actorReference', private.platform_admin_reference(actor_user_id),
          'subjectReference', case
            when subject_user_id is null then null
            else private.platform_admin_reference(subject_user_id)
          end,
          'invitationCode', invitation_code,
          'previousRole', previous_role,
          'nextRole', next_role,
          'createdAt', created_at
        ) order by created_at desc
      )
      from (
        select
          paa.id as event_id,
          paa.action,
          paa.actor_user_id,
          paa.subject_user_id,
          pai.invitation_code,
          paa.previous_role,
          paa.next_role,
          paa.created_at
        from private.platform_admin_access_audit paa
        left join private.platform_admin_invitations pai on pai.id = paa.invitation_id
        order by paa.created_at desc
        limit 30
      ) events
    ), '[]'::jsonb)
  );
end;
$$;

