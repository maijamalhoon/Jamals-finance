create or replace function private.purge_expired_privacy_operational_data(
  p_initiated_by text default 'service_role',
  p_actor_admin_user_id uuid default null
)
returns jsonb
language plpgsql
security definer
set search_path = pg_catalog, private, telemetry
as $$
declare
  v_telemetry_deleted bigint := 0;
  v_admin_logs_deleted bigint := 0;
  v_request_audit_deleted bigint := 0;
  v_old_runs_deleted bigint := 0;
begin
  if p_initiated_by not in ('scheduled', 'service_role', 'manual') then
    raise exception 'privacy_retention_initiator_invalid' using errcode = '22023';
  end if;

  delete from telemetry.events
  where expires_at <= now();
  get diagnostics v_telemetry_deleted = row_count;

  delete from private.admin_access_log
  where expires_at <= now();
  get diagnostics v_admin_logs_deleted = row_count;

  delete from private.privacy_request_audit
  where expires_at <= now();
  get diagnostics v_request_audit_deleted = row_count;

  delete from private.privacy_retention_runs
  where expires_at <= now();
  get diagnostics v_old_runs_deleted = row_count;

  insert into private.privacy_retention_runs (
    initiated_by,
    actor_admin_user_id,
    telemetry_events_deleted,
    admin_access_logs_deleted,
    request_audit_logs_deleted,
    prior_retention_runs_deleted
  )
  values (
    p_initiated_by,
    p_actor_admin_user_id,
    v_telemetry_deleted,
    v_admin_logs_deleted,
    v_request_audit_deleted,
    v_old_runs_deleted
  );

  return jsonb_build_object(
    'telemetryEventsDeleted', v_telemetry_deleted,
    'adminAccessLogsDeleted', v_admin_logs_deleted,
    'requestAuditLogsDeleted', v_request_audit_deleted,
    'priorRetentionRunsDeleted', v_old_runs_deleted,
    'ranAt', now()
  );
end;
$$;

revoke all on function private.purge_expired_privacy_operational_data(text, uuid)
  from public, anon, authenticated;
grant execute on function private.purge_expired_privacy_operational_data(text, uuid)
  to service_role;

create or replace function private.get_privacy_governance_snapshot()
returns jsonb
language plpgsql
stable
security definer
set search_path = pg_catalog, auth, private, telemetry
as $$
declare
  v_admin_user_id uuid := auth.uid();
  v_admin_role text;
  v_last_run private.privacy_retention_runs%rowtype;
begin
  select role
  into v_admin_role
  from private.platform_admins
  where user_id = v_admin_user_id
    and disabled_at is null;

  if v_admin_user_id is null or v_admin_role is null then
    raise exception 'admin_access_required' using errcode = '42501';
  end if;

  select *
  into v_last_run
  from private.privacy_retention_runs
  order by ran_at desc
  limit 1;

  return jsonb_build_object(
    'openRequests', (
      select count(*)::bigint
      from private.privacy_requests
      where status in ('pending', 'identity_verification', 'in_progress')
    ),
    'overdueRequests', (
      select count(*)::bigint
      from private.privacy_requests
      where status in ('pending', 'identity_verification', 'in_progress')
        and due_at is not null
        and due_at < now()
    ),
    'completedRequests30d', (
      select count(*)::bigint
      from private.privacy_requests
      where status = 'completed'
        and closed_at >= now() - interval '30 days'
    ),
    'requestAuditEvents30d', (
      select count(*)::bigint
      from private.privacy_request_audit
      where created_at >= now() - interval '30 days'
    ),
    'requestOperationsAllowed', v_admin_role in ('owner', 'admin'),
    'requestQueue', coalesce((
      select jsonb_agg(
        jsonb_build_object(
          'requestCode', queue.request_code,
          'requestType', queue.request_type,
          'status', queue.status,
          'verificationStatus', queue.verification_status,
          'source', queue.source,
          'createdAt', queue.created_at,
          'dueAt', queue.due_at,
          'assigned', queue.assigned_admin_user_id is not null,
          'assignedToMe', queue.assigned_admin_user_id = v_admin_user_id,
          'overdue', queue.due_at is not null and queue.due_at < now(),
          'manageable', (
            v_admin_role = 'owner'
            or queue.assigned_admin_user_id is null
            or queue.assigned_admin_user_id = v_admin_user_id
          )
        ) order by
          (queue.due_at is not null and queue.due_at < now()) desc,
          queue.due_at asc nulls last,
          queue.created_at asc
      )
      from (
        select
          request_code,
          request_type,
          status,
          verification_status,
          source,
          assigned_admin_user_id,
          created_at,
          due_at
        from private.privacy_requests
        where status in ('pending', 'identity_verification', 'in_progress')
        order by
          (due_at is not null and due_at < now()) desc,
          due_at asc nulls last,
          created_at asc
        limit 30
      ) as queue
    ), '[]'::jsonb),
    'adminViews30d', (
      select count(*)::bigint
      from private.admin_access_log
      where created_at >= now() - interval '30 days'
    ),
    'telemetryEventsStored', (
      select count(*)::bigint from telemetry.events
    ),
    'telemetrySubjectsStored', (
      select count(*)::bigint from telemetry.subjects
    ),
    'expiredTelemetryPending', (
      select count(*)::bigint
      from telemetry.events
      where expires_at <= now()
    ),
    'expiredAdminAuditPending', (
      select count(*)::bigint
      from private.admin_access_log
      where expires_at <= now()
    ),
    'expiredRequestAuditPending', (
      select count(*)::bigint
      from private.privacy_request_audit
      where expires_at <= now()
    ),
    'lastRetentionRunAt', v_last_run.ran_at,
    'lastRetentionRowsDeleted', coalesce(
      v_last_run.telemetry_events_deleted
      + v_last_run.admin_access_logs_deleted
      + v_last_run.request_audit_logs_deleted
      + v_last_run.prior_retention_runs_deleted,
      0
    ),
    'telemetryRetentionDays', 30,
    'adminAuditRetentionMonths', 12,
    'requestAuditRetentionMonths', 12,
    'rawIpStored', false,
    'sessionReplayEnabled', false,
    'financeContentInTelemetry', false
  );
end;
$$;

revoke all on function private.get_privacy_governance_snapshot()
  from public, anon;
grant execute on function private.get_privacy_governance_snapshot()
  to authenticated;

