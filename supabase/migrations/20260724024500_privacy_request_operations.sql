create or replace function private.apply_privacy_request_workflow(
  p_request_code text,
  p_status text,
  p_verification_status text,
  p_due_date date default null,
  p_assign_to_self boolean default false
)
returns jsonb
language plpgsql
security definer
set search_path = pg_catalog, auth, private
as $$
declare
  v_admin_user_id uuid := auth.uid();
  v_admin_role text;
  v_request private.privacy_requests%rowtype;
  v_next_due_at timestamptz;
  v_assignment_changed boolean := false;
begin
  select role
  into v_admin_role
  from private.platform_admins
  where user_id = v_admin_user_id
    and disabled_at is null;

  if v_admin_user_id is null or v_admin_role not in ('owner', 'admin') then
    raise exception 'privacy_request_management_required' using errcode = '42501';
  end if;

  if p_request_code is null or p_request_code !~ '^PRV-[A-F0-9]{12}$' then
    raise exception 'privacy_request_code_invalid' using errcode = '22023';
  end if;

  if p_status not in (
    'pending', 'identity_verification', 'in_progress',
    'completed', 'rejected', 'cancelled'
  ) then
    raise exception 'privacy_request_status_invalid' using errcode = '22023';
  end if;

  if p_verification_status not in ('not_started', 'pending', 'verified', 'failed') then
    raise exception 'privacy_request_verification_invalid' using errcode = '22023';
  end if;

  select *
  into v_request
  from private.privacy_requests
  where request_code = p_request_code
  for update;

  if not found then
    raise exception 'privacy_request_not_found' using errcode = 'P0002';
  end if;

  if v_request.assigned_admin_user_id is not null
     and v_request.assigned_admin_user_id <> v_admin_user_id
     and v_admin_role <> 'owner' then
    raise exception 'privacy_request_assigned_elsewhere' using errcode = '42501';
  end if;

  if p_status = 'completed' and p_verification_status <> 'verified' then
    raise exception 'privacy_request_verification_required' using errcode = '22023';
  end if;

  if p_due_date is not null and p_due_date < v_request.created_at::date then
    raise exception 'privacy_request_due_date_invalid' using errcode = '22023';
  end if;

  if p_due_date is not null then
    v_next_due_at := (p_due_date::timestamptz + interval '1 day' - interval '1 second');
  end if;

  v_assignment_changed := p_assign_to_self
    and v_request.assigned_admin_user_id is distinct from v_admin_user_id;

  update private.privacy_requests
  set status = p_status,
      verification_status = p_verification_status,
      due_at = v_next_due_at,
      assigned_admin_user_id = case
        when p_assign_to_self then v_admin_user_id
        else assigned_admin_user_id
      end,
      closed_at = case
        when p_status in ('completed', 'rejected', 'cancelled')
          then coalesce(closed_at, now())
        else null
      end,
      updated_at = now()
  where id = v_request.id;

  insert into private.privacy_request_audit (
    request_id,
    actor_admin_user_id,
    action,
    previous_status,
    next_status,
    previous_verification_status,
    next_verification_status,
    assignment_changed,
    previous_due_at,
    next_due_at
  )
  values (
    v_request.id,
    v_admin_user_id,
    case when v_assignment_changed
      then 'claimed_and_updated'
      else 'workflow_updated'
    end,
    v_request.status,
    p_status,
    v_request.verification_status,
    p_verification_status,
    v_assignment_changed,
    v_request.due_at,
    v_next_due_at
  );

  return jsonb_build_object(
    'requestCode', v_request.request_code,
    'status', p_status,
    'verificationStatus', p_verification_status,
    'dueAt', v_next_due_at,
    'assignedToMe', (
      p_assign_to_self or v_request.assigned_admin_user_id = v_admin_user_id
    ),
    'updatedAt', now()
  );
end;
$$;

