create table if not exists private.privacy_request_audit (
  id bigint generated by default as identity primary key,
  request_id uuid not null references private.privacy_requests(id) on delete cascade,
  actor_admin_user_id uuid references auth.users(id) on delete set null,
  action text not null default 'workflow_updated',
  previous_status text not null,
  next_status text not null,
  previous_verification_status text not null,
  next_verification_status text not null,
  assignment_changed boolean not null default false,
  previous_due_at timestamptz,
  next_due_at timestamptz,
  created_at timestamptz not null default now(),
  expires_at timestamptz not null default (now() + interval '12 months'),
  constraint privacy_request_audit_action_check check (
    action in ('workflow_updated', 'claimed_and_updated')
  ),
  constraint privacy_request_audit_status_check check (
    previous_status in (
      'pending', 'identity_verification', 'in_progress',
      'completed', 'rejected', 'cancelled'
    )
    and next_status in (
      'pending', 'identity_verification', 'in_progress',
      'completed', 'rejected', 'cancelled'
    )
  ),
  constraint privacy_request_audit_verification_check check (
    previous_verification_status in ('not_started', 'pending', 'verified', 'failed')
    and next_verification_status in ('not_started', 'pending', 'verified', 'failed')
  )
);

comment on table private.privacy_request_audit is
  'Structured server-only audit of privacy workflow transitions. Contains no request message, end-user identity, finance content, raw IP address, or payment credential.';

