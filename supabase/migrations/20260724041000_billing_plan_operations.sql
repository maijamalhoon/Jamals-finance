create or replace function private.purge_expired_billing_operational_data()
returns jsonb
language plpgsql
security definer
set search_path = pg_catalog, private, billing
as $$
declare
  v_webhook_events_deleted bigint := 0;
  v_plan_audit_deleted bigint := 0;
begin
  delete from billing.webhook_events
  where expires_at <= now();
  get diagnostics v_webhook_events_deleted = row_count;

  delete from private.billing_plan_audit
  where expires_at <= now();
  get diagnostics v_plan_audit_deleted = row_count;

  return jsonb_build_object(
    'webhookEventsDeleted', v_webhook_events_deleted,
    'planAuditEventsDeleted', v_plan_audit_deleted,
    'ranAt', now()
  );
end;
$$;

revoke all on function private.purge_expired_billing_operational_data()
  from public, anon, authenticated;
grant execute on function private.purge_expired_billing_operational_data()
  to service_role;

