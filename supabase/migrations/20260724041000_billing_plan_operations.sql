  add column if not exists currency_exponent smallint not null default 2;

do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conname = 'billing_plans_currency_exponent_check'
      and conrelid = 'billing.plans'::regclass
  ) then
    alter table billing.plans
      add constraint billing_plans_currency_exponent_check
      check (currency_exponent between 0 and 3);
  end if;
end;
$$;

comment on column billing.plans.currency_exponent is
  'Number of decimal places used to convert the displayed plan price into provider-neutral minor units.';

create table if not exists private.billing_plan_audit (
  id bigint generated by default as identity primary key,
  actor_admin_user_id uuid references auth.users(id) on delete set null,
  plan_code text not null,
  action text not null,
  before_name text,
  after_name text not null,
  before_billing_interval text,
  after_billing_interval text not null,
  before_price_minor bigint,
  after_price_minor bigint not null,
  before_currency text,
  after_currency text not null,
  before_currency_exponent smallint,
  after_currency_exponent smallint not null,
  before_is_active boolean,
  after_is_active boolean not null,
  created_at timestamptz not null default now(),
  expires_at timestamptz not null default (now() + interval '24 months'),
  constraint billing_plan_audit_code_check check (
    plan_code ~ '^[a-z0-9][a-z0-9_-]{1,39}$'
  ),
  constraint billing_plan_audit_action_check check (
    action in ('created', 'updated', 'activated', 'deactivated')
  ),
  constraint billing_plan_audit_name_check check (
    char_length(after_name) between 1 and 80
    and (before_name is null or char_length(before_name) between 1 and 80)
  ),
  constraint billing_plan_audit_interval_check check (
    after_billing_interval in ('month', 'year', 'one_time')
    and (
      before_billing_interval is null
      or before_billing_interval in ('month', 'year', 'one_time')
    )
  ),
  constraint billing_plan_audit_price_check check (
    after_price_minor > 0
    and (before_price_minor is null or before_price_minor > 0)
  ),
  constraint billing_plan_audit_currency_check check (
    after_currency ~ '^[A-Z]{3}$'
    and (before_currency is null or before_currency ~ '^[A-Z]{3}$')
  ),
  constraint billing_plan_audit_exponent_check check (
    after_currency_exponent between 0 and 3
    and (
      before_currency_exponent is null
      or before_currency_exponent between 0 and 3
    )
  ),
  constraint billing_plan_audit_expiry_check check (expires_at >= created_at)
);

comment on table private.billing_plan_audit is
  'Owner-only plan-catalog audit. Stores commercial plan metadata only; never subscriber identity, card data, payment credentials, finance records, or raw webhook payloads.';

