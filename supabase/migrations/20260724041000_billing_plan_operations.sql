create or replace function private.apply_billing_plan_operation_impl(
  p_code text,
  p_name text,
  p_billing_interval text,
  p_price_major numeric,
  p_currency text,
  p_currency_exponent smallint default 2,
  p_is_active boolean default true
)
returns jsonb
language plpgsql
security definer
set search_path = pg_catalog, auth, private, billing
as $$
declare
  v_admin_user_id uuid := auth.uid();
  v_admin_role text;
  v_code text := lower(btrim(p_code));
  v_name text := btrim(p_name);
  v_interval text := lower(btrim(p_billing_interval));
  v_currency text := upper(btrim(p_currency));
  v_exponent smallint := coalesce(p_currency_exponent, 2);
  v_price_minor bigint;
  v_before billing.plans%rowtype;
  v_after billing.plans%rowtype;
  v_action text;
begin
  select role
  into v_admin_role
  from private.platform_admins
  where user_id = v_admin_user_id
    and disabled_at is null;

  if v_admin_user_id is null or v_admin_role <> 'owner' then
    raise exception 'billing_owner_access_required' using errcode = '42501';
  end if;

  if v_code is null
     or v_code !~ '^[a-z0-9][a-z0-9_-]{1,39}$'
     or v_code = 'free' then
    raise exception 'billing_plan_code_invalid' using errcode = '22023';
  end if;

  if v_name is null
     or char_length(v_name) < 1
     or char_length(v_name) > 80
     or v_name ~ '[[:cntrl:]]' then
    raise exception 'billing_plan_name_invalid' using errcode = '22023';
  end if;

  if v_interval not in ('month', 'year', 'one_time') then
    raise exception 'billing_plan_interval_invalid' using errcode = '22023';
  end if;

  if v_currency is null or v_currency !~ '^[A-Z]{3}$' then
    raise exception 'billing_plan_currency_invalid' using errcode = '22023';
  end if;

  if v_exponent not between 0 and 3 then
    raise exception 'billing_plan_currency_exponent_invalid' using errcode = '22023';
  end if;

  if p_price_major is null
     or p_price_major <= 0
     or p_price_major > 1000000000 then
    raise exception 'billing_plan_price_invalid' using errcode = '22023';
  end if;

  if p_is_active is null then
    raise exception 'billing_plan_active_state_invalid' using errcode = '22023';
  end if;

  v_price_minor := round(
    p_price_major * power(10::numeric, v_exponent)
  )::bigint;

  if v_price_minor <= 0 then
    raise exception 'billing_plan_price_invalid' using errcode = '22023';
  end if;

  select *
  into v_before
  from billing.plans
  where code = v_code
  for update;

  if found and v_before.plan_kind <> 'paid' then
    raise exception 'billing_plan_kind_locked' using errcode = '22023';
  end if;

  if not found then
    insert into billing.plans (
      code,
      name,
      plan_kind,
      billing_interval,
      price_minor,
      currency,
      currency_exponent,
      is_active,
      updated_at
    )
    values (
      v_code,
      v_name,
      'paid',
      v_interval,
      v_price_minor,
      v_currency,
      v_exponent,
      p_is_active,
      now()
    )
    returning * into v_after;

    v_action := 'created';
  else
    update billing.plans
    set name = v_name,
        billing_interval = v_interval,
        price_minor = v_price_minor,
        currency = v_currency,
        currency_exponent = v_exponent,
        is_active = p_is_active,
        updated_at = now()
    where code = v_code
    returning * into v_after;

    v_action := case
      when v_before.is_active = false and v_after.is_active = true then 'activated'
      when v_before.is_active = true and v_after.is_active = false then 'deactivated'
      else 'updated'
    end;
  end if;

  insert into private.billing_plan_audit (
    actor_admin_user_id,
    plan_code,
    action,
    before_name,
    after_name,
    before_billing_interval,
    after_billing_interval,
    before_price_minor,
    after_price_minor,
    before_currency,
    after_currency,
    before_currency_exponent,
    after_currency_exponent,
    before_is_active,
    after_is_active
  )
  values (
    v_admin_user_id,
    v_after.code,
    v_action,
    case when v_before.code is null then null else v_before.name end,
    v_after.name,
    case when v_before.code is null then null else v_before.billing_interval end,
    v_after.billing_interval,
    case when v_before.code is null then null else v_before.price_minor end,
    v_after.price_minor,
    case when v_before.code is null then null else v_before.currency end,
    v_after.currency,
    case when v_before.code is null then null else v_before.currency_exponent end,
    v_after.currency_exponent,
    case when v_before.code is null then null else v_before.is_active end,
    v_after.is_active
  );

  return jsonb_build_object(
    'code', v_after.code,
    'name', v_after.name,
    'kind', v_after.plan_kind,
    'billingInterval', v_after.billing_interval,
    'priceMajor', round(
      v_after.price_minor::numeric
      / power(10::numeric, v_after.currency_exponent),
      v_after.currency_exponent
    ),
    'currency', v_after.currency,
    'currencyExponent', v_after.currency_exponent,
    'isActive', v_after.is_active,
    'action', v_action,
    'featurePolicy', 'unlimited'
  );
end;
$$;

