create or replace function private.get_billing_operations_snapshot()
returns jsonb
language plpgsql
stable
security definer
set search_path = pg_catalog, auth, private, billing
as $$
declare
  v_admin_user_id uuid := auth.uid();
  v_admin_role text;
begin
  select role
  into v_admin_role
  from private.platform_admins
  where user_id = v_admin_user_id
    and disabled_at is null;

  if v_admin_user_id is null or v_admin_role is null then
    raise exception 'admin_access_required' using errcode = '42501';
  end if;

  return jsonb_build_object(
    'operationsAllowed', v_admin_role = 'owner',
    'providerConnected', exists (
      select 1
      from billing.customers
      where provider <> 'none'
        and provider_customer_id is not null
    ),
    'planCatalog', coalesce((
      select jsonb_agg(
        jsonb_build_object(
          'code', plans.code,
          'name', plans.name,
          'kind', plans.plan_kind,
          'billingInterval', plans.billing_interval,
          'priceMajor', plans.price_major,
          'currency', plans.currency,
          'currencyExponent', plans.currency_exponent,
          'isActive', plans.is_active,
          'totalUsers', plans.total_users,
          'trialUsers', plans.trial_users,
          'activeUsers', plans.active_users,
          'pastDueUsers', plans.past_due_users,
          'cancelledUsers', plans.cancelled_users,
          'editable', v_admin_role = 'owner' and plans.code <> 'free'
        )
        order by plans.is_active desc, plans.plan_kind, plans.code
      )
      from (
        select
          p.code,
          p.name,
          p.plan_kind,
          p.billing_interval,
          case
            when p.price_minor is null then null
            else round(
              p.price_minor::numeric / power(10::numeric, p.currency_exponent),
              p.currency_exponent
            )
          end as price_major,
          p.currency,
          p.currency_exponent,
          p.is_active,
          count(s.id)::bigint as total_users,
          count(s.id) filter (where s.status = 'trialing')::bigint as trial_users,
          count(s.id) filter (where s.status = 'active')::bigint as active_users,
          count(s.id) filter (where s.status = 'past_due')::bigint as past_due_users,
          count(s.id) filter (
            where s.status in ('cancelled', 'expired')
          )::bigint as cancelled_users
        from billing.plans p
        left join billing.subscriptions s on s.plan_code = p.code
        group by
          p.code,
          p.name,
          p.plan_kind,
          p.billing_interval,
          p.price_minor,
          p.currency,
          p.currency_exponent,
          p.is_active
        limit 40
      ) as plans
    ), '[]'::jsonb),
    'webhooks', jsonb_build_object(
      'received24h', (
        select count(*)::bigint
        from billing.webhook_events
        where received_at >= now() - interval '24 hours'
      ),
      'pending', (
        select count(*)::bigint
        from billing.webhook_events
        where processing_status = 'received'
      ),
      'processed24h', (
        select count(*)::bigint
        from billing.webhook_events
        where processing_status = 'processed'
          and processed_at >= now() - interval '24 hours'
      ),
      'failed24h', (
        select count(*)::bigint
        from billing.webhook_events
        where processing_status = 'failed'
          and received_at >= now() - interval '24 hours'
      ),
      'ignored24h', (
        select count(*)::bigint
        from billing.webhook_events
        where processing_status = 'ignored'
          and received_at >= now() - interval '24 hours'
      ),
      'lastReceivedAt', (
        select max(received_at) from billing.webhook_events
      ),
      'lastProcessedAt', (
        select max(processed_at) from billing.webhook_events
      ),
      'expiredPending', (
        select count(*)::bigint
        from billing.webhook_events
        where expires_at <= now()
      )
    ),
    'auditEvents30d', (
      select count(*)::bigint
      from private.billing_plan_audit
      where created_at >= now() - interval '30 days'
    ),
    'expiredAuditPending', (
      select count(*)::bigint
      from private.billing_plan_audit
      where expires_at <= now()
    ),
    'rawWebhookPayloadStored', false,
    'cardDataStored', false,
    'featureLimitsAttached', false
  );
end;
$$;

