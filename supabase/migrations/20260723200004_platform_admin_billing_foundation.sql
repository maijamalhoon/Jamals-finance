create or replace function private.get_platform_admin_snapshot()
returns jsonb
language plpgsql
security definer
set search_path = pg_catalog, auth, private, billing, telemetry
as $$
declare
  v_admin_user_id uuid := auth.uid();
  v_admin_role text;
  v_plan_breakdown jsonb := '[]'::jsonb;
  v_device_breakdown jsonb := '[]'::jsonb;
  v_country_breakdown jsonb := '[]'::jsonb;
  v_top_routes jsonb := '[]'::jsonb;
  v_slow_routes jsonb := '[]'::jsonb;
begin
  select role
  into v_admin_role
  from private.platform_admins
  where user_id = v_admin_user_id
    and disabled_at is null;

  if v_admin_user_id is null or v_admin_role is null then
    raise exception 'admin_access_required' using errcode = '42501';
  end if;

  insert into private.admin_access_log (admin_user_id, action, route)
  values (v_admin_user_id, 'control_center_viewed', '/admin');

  select coalesce(
    jsonb_agg(
      jsonb_build_object(
        'code', code,
        'name', name,
        'kind', plan_kind,
        'status', status,
        'users', users
      )
      order by users desc, code
    ),
    '[]'::jsonb
  )
  into v_plan_breakdown
  from (
    select
      p.code,
      p.name,
      p.plan_kind,
      s.status,
      count(*)::bigint as users
    from billing.subscriptions s
    join billing.plans p on p.code = s.plan_code
    group by p.code, p.name, p.plan_kind, s.status
  ) plan_rows;

  select coalesce(
    jsonb_agg(
      jsonb_build_object('device', device_type, 'users', users)
      order by users desc
    ),
    '[]'::jsonb
  )
  into v_device_breakdown
  from (
    select device_type, count(distinct subject_id)::bigint as users
    from telemetry.events
    where occurred_at >= now() - interval '30 days'
    group by device_type
    order by users desc
    limit 6
  ) device_rows;

  select coalesce(
    jsonb_agg(
      jsonb_build_object('country', country_code, 'users', users)
      order by users desc
    ),
    '[]'::jsonb
  )
  into v_country_breakdown
  from (
    select country_code, count(distinct subject_id)::bigint as users
    from telemetry.events
    where occurred_at >= now() - interval '30 days'
      and country_code is not null
    group by country_code
    order by users desc
    limit 8
  ) country_rows;

  select coalesce(
    jsonb_agg(
      jsonb_build_object('route', route, 'events', events)
      order by events desc
    ),
    '[]'::jsonb
  )
  into v_top_routes
  from (
    select route, count(*)::bigint as events
    from telemetry.events
    where occurred_at >= now() - interval '7 days'
    group by route
    order by events desc
    limit 8
  ) route_rows;

  select coalesce(
    jsonb_agg(
      jsonb_build_object('route', route, 'signals', signals)
      order by signals desc
    ),
    '[]'::jsonb
  )
  into v_slow_routes
  from (
    select route, count(*)::bigint as signals
    from telemetry.events
    where occurred_at >= now() - interval '7 days'
      and (
        (event_name = 'web_vital' and metric_rating = 'poor')
        or event_name = 'long_task'
      )
    group by route
    order by signals desc
    limit 8
  ) slow_rows;

  return jsonb_build_object(
    'generatedAt', now(),
    'adminRole', v_admin_role,
    'featurePolicy', 'unlimited',
    'users', jsonb_build_object(
      'total', (
        select count(*)::bigint
        from auth.users
        where deleted_at is null
          and coalesce(is_anonymous, false) = false
      ),
      'new7d', (
        select count(*)::bigint
        from auth.users
        where deleted_at is null
          and coalesce(is_anonymous, false) = false
          and created_at >= now() - interval '7 days'
      ),
      'new30d', (
        select count(*)::bigint
        from auth.users
        where deleted_at is null
          and coalesce(is_anonymous, false) = false
          and created_at >= now() - interval '30 days'
      ),
      'signedIn24h', (
        select count(*)::bigint
        from auth.users
        where deleted_at is null
          and coalesce(is_anonymous, false) = false
          and last_sign_in_at >= now() - interval '24 hours'
      ),
      'signedIn30d', (
        select count(*)::bigint
        from auth.users
        where deleted_at is null
          and coalesce(is_anonymous, false) = false
          and last_sign_in_at >= now() - interval '30 days'
      )
    ),
    'billing', jsonb_build_object(
      'freeUsers', (
        select count(*)::bigint
        from billing.subscriptions
        where status = 'free'
      ),
      'trialUsers', (
        select count(*)::bigint
        from billing.subscriptions
        where status = 'trialing'
      ),
      'paidUsers', (
        select count(*)::bigint
        from billing.subscriptions s
        join billing.plans p on p.code = s.plan_code
        where p.plan_kind = 'paid'
          and s.status = 'active'
      ),
      'pastDueUsers', (
        select count(*)::bigint
        from billing.subscriptions
        where status = 'past_due'
      ),
      'cancelledUsers', (
        select count(*)::bigint
        from billing.subscriptions
        where status in ('cancelled', 'expired')
      ),
      'providerConnected', exists (
        select 1
        from billing.customers
        where provider <> 'none'
          and provider_customer_id is not null
      ),
      'plans', v_plan_breakdown
    ),
    'telemetry', jsonb_build_object(
      'activeUsers24h', (
        select count(distinct subject_id)::bigint
        from telemetry.events
        where occurred_at >= now() - interval '24 hours'
      ),
      'activeUsers30d', (
        select count(distinct subject_id)::bigint
        from telemetry.events
        where occurred_at >= now() - interval '30 days'
      ),
      'events24h', (
        select count(*)::bigint
        from telemetry.events
        where occurred_at >= now() - interval '24 hours'
      ),
      'failedOperations7d', (
        select count(*)::bigint
        from telemetry.events
        where occurred_at >= now() - interval '7 days'
          and event_name = 'operation_failed'
      ),
      'poorPerformanceSignals7d', (
        select count(*)::bigint
        from telemetry.events
        where occurred_at >= now() - interval '7 days'
          and (
            (event_name = 'web_vital' and metric_rating = 'poor')
            or event_name = 'long_task'
          )
      ),
      'devices', v_device_breakdown,
      'countries', v_country_breakdown,
      'topRoutes', v_top_routes,
      'slowRoutes', v_slow_routes
    )
  );
end;
$$;

